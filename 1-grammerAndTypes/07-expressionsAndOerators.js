//Assignment operator
let x = 5;

//addition assignment
x += 1; //x = x + 1;
console.log(x);

//subtraction assignment
x -= 1; //x = x- 1;
console.log(x);

//multiplications assignment
x *= 1; //x = x * 1;
console.log(x);

//Division assignment
x /= 1; //x = x / 1;
console.log(x);

//Remainder (or Modulus) assignment
x %= 1; //x = x % 1;
console.log(x);

//Exponential assignment
x **= 1; //x = x ** 1;
console.log(x);



//Comparison Operators

//Equal
console.log("3" == 3); //true

//Strictly equal
console.log("3" === 3); //false

//Not equal
console.log("3" != 3);

//Strictly not equal
console.log("3" !== 3);

//Greater than
console.log(3>2);

//Less thank
console.log(3<2);

//Greater than or equal to
console.log(3 >= 2);

//Less than or equal to
console.log(3 <= 2);

//Or
console.log(3 || 2);

//And
console.log(3&&2);